package RPG;

public class StatusEffect {
    
    private int level;
    private int turnDuration;
    private int baseDamage;
    
    public int getLevel() {
        return level;
    }
    
    public int getTurnDuration() {
        return turnDuration;
    }
    
    public int getBaseDamage() {
        return baseDamage;
    }
    
}